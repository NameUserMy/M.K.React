export class Card{

     Title;
     Released;
     Genre;
     Country;
     Director;
     Writer;
     Actors;
     Awards;
     Poster;
    constructor(title,released,genre,
        country,director,writer,actors,awards,poster){

            this.Title=title;
            this.Released=released;
            this.Genre=genre;
            this.Country=country;
            this.Director=director;
            this.Writer=writer;
            this.Actors=actors;
            this.Awards=awards;
            this.Poster=poster;

    }


}